export const dict: Record<SupportedLang, Dict> = { de, en, ru, el, zh };

export function makeT(lang: SupportedLang) {
  const map = dict[lang] ?? dict[DEFAULT_LANG];
  return (key: TKey): string => map[key] ?? dict[DEFAULT_LANG][key];
}

export type T = ReturnType<typeof makeT>;

/**
 * Ersetzt Platzhalter wie {n} oder {region} durch Werte. Stark vereinfachte
 * Variante — alles wird zu String konvertiert.
 */
export function tFormat(template: string, vars: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, k) => {
    const v = vars[k];
    return v == null ? `{${k}}` : String(v);
  });
}
